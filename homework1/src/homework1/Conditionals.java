package homework1;

public class Conditionals {

	public static void main(String[] args) {
		int number = 20;
		
		if(number < 20){
			System.out.println("Number less than 20");
		}
		else if (number > 20) {
			System.out.println("Number bigger than 20");
		}
		else {
			System.out.println("Number is equal to 20");
		}

	}

}
